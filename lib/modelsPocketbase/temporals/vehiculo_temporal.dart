
import 'package:taller_alex_app_asesor/database/entitys.dart';

class VehiculoTemporal {
    final String marca;
    final String modelo;
    final String anio;
    final String vin;
    final String placas;
    final String motor;
    final String color;
    final String imagen;
    final String path;

    VehiculoTemporal({
      required this.marca,
      required this.modelo,
      required this.anio,
      required this.vin,
      required this.placas,
      required this.motor,
      required this.color,
      required this.imagen,
      required this.path,
    });

}
